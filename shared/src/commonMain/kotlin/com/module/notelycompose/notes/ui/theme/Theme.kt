/**
 * Clean Material 3 Theme for Notely Capture
 * 
 * Uses Material Theme Builder generated colors for consistent design.
 * No custom accent colors - relies on Material 3 design tokens.
 */
@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) darkScheme else lightScheme
    
    // Material 3 semantic custom colors for component-specific styling
    val customColors = if (darkTheme) DarkCustomColors else LightCustomColors
    
    // Material 3 Expressive Typography with Poppins font family
    val typography = createMaterial3ExpressiveTypography()
    
    // Material 3 Expressive Shapes with 5-tier system
    val shapes = createMaterial3ExpressiveShapes()

    CompositionLocalProvider(LocalCustomColors provides customColors) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = typography,
            shapes = shapes,
            content = content
        )
    }
}